package sorting_prog;

public class Beanproduct1 
{
	public int price;
	public String brand;
	public String color;

		public Beanproduct1 (int price, String brand, String color)
		{
			this.price=price;
			this.brand=brand;
			this.color=color;
		}

			public int getprice()
			{
				return price;
			}

			public String getbrand()
			{
				return brand;
			}

			public String getcolor()
			{
				return color;
			}
}
